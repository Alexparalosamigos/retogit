public class operaciones {
    public static void main (String[] args){
        int num1 = 10;
        int num2 = 2;
        int suma = num1 + num2;
        int multiplicación = num1 * num2;
        int división = num1 / num2;
        int resto = num1 % num2;


        System.out.println("El valor del número uno es "+num1);
        System.out.println("El valor del número dos es "+num2);
        System.out.println("El resultado de la suma es "+suma);
        System.out.println("El resultado de la multiplicación es "+multiplicación);
        System.out.println("El resultado de la división es "+división);
        System.out.println("El resto de la división es "+resto);

    }
}

